import * as React from 'react';
import { styled, alpha } from '@mui/material/styles';
import Box from '@mui/material/Box';
import InputBase from '@mui/material/InputBase';
import SearchIcon from '@mui/icons-material/Search';
import { mangaActions } from '../context/mangaSlice';
import { useDispatch } from 'react-redux';

const Search = styled('div')(({ theme }) => ({
  position: 'relative',
  borderRadius: theme.shape.borderRadius,
  backgroundColor: alpha(theme.palette.common.white, 0.15),
  '&:hover': {
    backgroundColor: alpha(theme.palette.common.white, 0.25),
  },
  marginLeft: 0,
  width: '100%',
  [theme.breakpoints.up('sm')]: {
    marginLeft: theme.spacing(5),
    width: '60%',
    float: 'right'
  },
}));

const SearchIconWrapper = styled('div')(({ theme }) => ({
  padding: theme.spacing(0, 2),
  height: '100%',
  position: 'absolute',
  pointerEvents: 'none',
  display: 'flex',
  alignItems: 'center',
  justifyContent: 'center',
}));

const StyledInputBase = styled(InputBase)(({ theme }) => ({
  color: 'inherit',
  width: '100%',
  '& .MuiInputBase-input': {
    padding: theme.spacing(1, 1, 1, 0),
    // vertical padding + font size from searchIcon
    paddingLeft: `calc(1em + ${theme.spacing(4)})`,
    transition: theme.transitions.create('width'),
    [theme.breakpoints.up('sm')]: {
      width: '12ch',
      '&:focus': {
        width: '20ch',
      },
    },
  },
}));


export default function SearchBar() {
  const dispatch = useDispatch();
  
  const handleSearch = (ev) => {
    dispatch(mangaActions.set_search(ev.target.value));
    dispatch(mangaActions.set_pagination({ offset: 0 }));
  };
  
  const handleClear = () => {
    dispatch(mangaActions.set_search(''));
    dispatch(mangaActions.set_pagination({ offset: 0 }));
  };
  
  
  return (
    <Box sx={{ flexGrow: 1 }}>
      <Search onKeyUp={(ev) => (ev.target.value !== '') ? handleSearch(ev) : handleClear()}>
        <SearchIconWrapper>
          <SearchIcon />
        </SearchIconWrapper>
        <StyledInputBase
          placeholder="Searchâ€¦"
          inputProps={{ 'aria-label': 'search' }}
        />
      </Search>
    </Box>
  );
}
